package com.mysentosa.android.sg.models;

public class IslanderUser {
    int Id;
    String NRICNumber;
    String Email;
    String Token;
    String TokenExpiredTime;
    String FullName;
    String AccountNumber;
    String MobilePhone;
    String Address;
    String MembershipExpiry;
    
    public int getId() {
        return Id;
    }
    public void setId(int iD) {
        Id = iD;
    }
    public String getNRICNumber() {
        return NRICNumber;
    }
    public void setNRICNumber(String nRICNumber) {
        NRICNumber = nRICNumber;
    }
    public String getEmail() {
        return Email;
    }
    public void setEmail(String email) {
        Email = email;
    }
    public String getToken() {
        return Token;
    }
    public void setToken(String token) {
        Token = token;
    }
    public String getTokenExpiredTime() {
        return TokenExpiredTime;
    }
    public void setTokenExpiredTime(String tokenExpiredTime) {
        TokenExpiredTime = tokenExpiredTime;
    }
    public String getFullName() {
        return FullName;
    }
    public void setFullName(String fullName) {
        FullName = fullName;
    }
    public String getAccountNumber() {
        return AccountNumber;
    }
    public void setAccountNumber(String accountNumber) {
        AccountNumber = accountNumber;
    }
    public String getMobilePhone() {
        return MobilePhone;
    }
    public void setMobilePhone(String mobilePhone) {
        MobilePhone = mobilePhone;
    }
    public String getAddress() {
        return Address;
    }
    public void setAddress(String address) {
        Address = address;
    }
    public String getMembershipExpiry() {
        return MembershipExpiry;
    }
    public void setMembershipExpiry(String membershipExpiry) {
        MembershipExpiry = membershipExpiry;
    }
    
    
}
